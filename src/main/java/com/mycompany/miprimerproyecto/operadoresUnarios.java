public class operadoresUnarios {
    public static void main(String[] args) {
        int x = 5;
        
        System.out.println("Valor inicial de x: " + x);
        System.out.println("x++: " + x++);
        System.out.println("Ahora x: " + x);
        System.out.println("++x: " + ++x);
        System.out.println("x--: " + x--);
        System.out.println("Ahora x: " + x);
    }
}
